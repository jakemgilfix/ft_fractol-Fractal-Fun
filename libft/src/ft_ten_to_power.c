/*
** FT_TEN_TO_POWER *************************************************************
** ~ Returns (10^n)L                                                          **
********************************************************************************
*/

long	ft_ten_to_power(int n)
{
	return (!n ? 1 : 10 * ft_ten_to_power(n - 1));
}
